1.SELECT * FROM CUSTOMER;
2.SELECT * FROM DRIVER;
3.SELECT * FROM VEHICLE;
4.SELECT * FROM RIDE;
5.SELECT * FROM PAYMENT;

6.SELECT *
FROM RIDE
WHERE RideStatus = 'Completed';

7.SELECT *
FROM RIDE
WHERE Fare > 2000;

8.SELECT *
FROM CUSTOMER
ORDER BY FullName;

9.SELECT *
FROM DRIVER
ORDER BY Salary DESC;

10.SELECT COUNT(*) AS Total_Rides
FROM RIDE;

11.SELECT C.FULLNAME,
       R.RIDEID,
       R.PICKUPLOCATION,
       R.DROPOFFLOCATION,
       R.FARE
FROM CUSTOMER C
JOIN RIDE R
ON C.CUSTOMER_ID = R.CUSTOMERID;

12.SELECT D.FULLNAME,
       R.RIDEID,
       R.RIDESTATUS,
       R.FARE
FROM DRIVER D
JOIN RIDE R
ON D.DRIVERID = R.DRIVERID;

13.SELECT V.BRAND,
       V.MODEL,
       V.REGISTRATIONNUMBER,
       R.RIDEID
FROM VEHICLE V
JOIN RIDE R
ON V.VEHICLEID = R.VEHICLEID;

14.SELECT R.RIDEID,
       P.PAYMENTMETHOD,
       P.AMOUNT,
       P.PAYMENTSTATUS
FROM RIDE R
JOIN PAYMENT P
ON R.RIDEID = P.RIDEID;

15.SELECT SUM(AMOUNT) AS TOTAL_EARNINGS
FROM PAYMENT
WHERE PAYMENTSTATUS='Paid';

16.SELECT AVG(FARE) AS AVERAGE_FARE
FROM RIDE;

17.SELECT MAX(FARE) AS HIGHEST_FARE
FROM RIDE;

18.SELECT D.FULLNAME,
       COUNT(R.RIDEID) AS TOTAL_RIDES
FROM DRIVER D
JOIN RIDE R
ON D.DRIVERID = R.DRIVERID
GROUP BY D.FULLNAME;

19.SELECT *
FROM PAYMENT
WHERE PAYMENTSTATUS='Pending';

20.SELECT C.FULLNAME AS CUSTOMER,
       D.FULLNAME AS DRIVER,
       V.BRAND,
       V.MODEL,
       R.PICKUPLOCATION,
       R.DROPOFFLOCATION,
       R.FARE,
       P.PAYMENTMETHOD,
       P.PAYMENTSTATUS
FROM CUSTOMER C
JOIN RIDE R
ON C.CUSTOMER_ID = R.CUSTOMERID
JOIN DRIVER D
ON D.DRIVERID = R.DRIVERID
JOIN VEHICLE V
ON V.VEHICLEID = R.VEHICLEID
JOIN PAYMENT P
ON P.RIDEID = R.RIDEID;
